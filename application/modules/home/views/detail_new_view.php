																				<p><em><strong>Không có bất ngờ lớn ở loạt trận vòng 5 diễn ra chiều nay (29/6), khi các đội đầu bảng đều có chiến thắng. Đấy chính là lý do mà thứ tự của nhóm đầu hiện không thay đổi…</strong></em></p>
																				<p>Trận đấu căng thẳng nhất trong ngày có lẽ là trận đấu giữa ĐKVĐ Thái Sơn Nam và Hải Phương Nam. Trận này kết thúc với thắng lợi sát nút 5-4 nghiêng về phía Thái Sơn Nam.</p>
																				<div id="attachment_4361" style="width: 510px" class="wp-caption aligncenter">
																					<a href="wp-content/uploads/2014/06/DatVinhTien_29-6-14.jpg"><img class="size-medium wp-image-4361" alt="Đạt Vĩnh Tiến giữ vững vị trí thứ 2" src="wp-content/uploads/2014/06/DatVinhTien_29-6-14-500x281.jpg" width="500" height="281" /></a>
																					<p class="wp-caption-text">Đạt Vĩnh Tiến giữ vững vị trí thứ 2</p>
																				</div>
																				<p>Dù thắng trận, nhưng Thái Sơn Nam vẫn chỉ cải thiện được vị trí thứ 5 của mình lên thứ 4 trên bảng xếp hạng, do 3 đội xếp trên họ đều thắng.</p>
																				<p>Đội đầu bảng Tân Hiệp Hưng không mấy khó khăn để giành thắng lợi đậm 3-0 trước Sài Gòn FC. Đây đã là chiến thắng thứ 5 liên tiếp của đội đầu bảng, giúp cho có số điểm tuyệt đối sau vòng 5.</p>
																				<p>Dù vậy, chiến thắng của Tân Hiệp Hưng vẫn chưa phải là chiến thắng đậm nhất vòng đấu. Chiến thắng đậm nhất chiều 29/6 là trận thắng 7-1 của Đạt Vĩnh Tiến trước Networld.</p>
																				<p>Ở một cặp đấu khác, đội bóng trẻ Metasol thắng Gỗ Trinh Thiên 7-4, qua đó tiếp tục đà tiến của mình.</p>
																				<p><strong>Kết quả các trận đấu ngày 29/6</strong></p>
																				<p>Networld – Đạt Vĩnh Tiến: 1-7</p>
																				<p>Hải Phương Nam – Thái Sơn Nam: 4-5</p>
																				<p>Tân Hiệp Hưng – Sài Gòn FC: 3-0</p>
																				<p>Metasol – Gỗ Trinh Thiên: 7-4</p>
